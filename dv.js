// Características:

// Tarjetas gráficas dedicadas (NVIDIA RTX o AMD Radeon).
// Pantallas de alta tasa de refresco (120 Hz o más).
// Procesadores Intel Core i7/i9 o AMD Ryzen 7/9.
// Diseño robusto y, a menudo, con retroiluminación RGB.
