// 2. Portátiles de gama media
// Descripción: Son portátiles que ofrecen un buen equilibrio entre rendimiento y precio. Están diseñados para usuarios que necesitan potencia para tareas diarias como navegación web, trabajo de oficina y consumo multimedia, pero sin llegar a ser tan caros como los ultrabooks de gama alta.

// Características:

// Pantallas de 14" a 15.6".
// Procesadores Intel Core i5 o AMD Ryzen 5.
// 8 GB a 16 GB de RAM.
// Baterías de duración media (6-10 horas).
// Ejemplos:

// Acer Aspire 5: Buen rendimiento para tareas cotidianas y entretenimiento, con opciones de pantalla Full HD.
// Lenovo IdeaPad 3: Ideal para estudiantes y usuarios de oficina que buscan una opción asequible y confiable.
// HP Pavilion x360: Convertible, con pantalla táctil, que ofrece versatilidad a un precio competitivo.
// 3. Portátiles para juegos (gaming)
